package Day2;

public class Datatype {

    public static void main(String[] args) {

        // (DataType) (Variable Name) = (Value) ;

        // 1. Numeric .......
        int a = 10;
        int b=-67;
        byte c=34;
        short d=18;
        long e=24537;

        float value1=64.56f;
        float value2=8.9f;
        float value5=45;
        double value3=45.56D;
        double value4=45.89;
        double value6=8;

        // Character
        // 1. Single quotation 2. only single value
        char char1='2';
        char char2='u';
        char char3='@';

        // String
        String string1="Mayukhjit2001_";
        String string2="Tanusree 200689";

        // boolean
        boolean boolean1=true;
        boolean boolean2=false;

        System.out.println(a);
        System.out.println(value2);
        System.out.println(value3);
        System.out.println(string1);
        System.out.println(char2);
        System.out.println(char1);
        System.out.println(boolean1);

    }
}
