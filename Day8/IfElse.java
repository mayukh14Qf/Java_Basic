package Day8;

public class IfElse {
    public static void main(String[] args) {

        // 1. If - Else
        //2. Switch case
        //3. Ternary operator

        // To check the condition
        // if ------> Relational / Logical operators (True / false)
        // else -------> No conditions
        // either if or else will run.

        int number=10;
        if (number==2){
            System.out.println("yes .......");
        }
        else {
            System.out.println("No ........");
            System.out.println("no ....2");
        }

    }
}