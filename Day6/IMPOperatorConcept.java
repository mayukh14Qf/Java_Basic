package Day6;

public class IMPOperatorConcept {
    public static void main(String[] args) {

        // If I am Doing with 10 (Integer )

        int var=1234;

        System.out.println(var/10); // all the digits except last digit
        System.out.println(var%10); // only the last digit

        // If we have a float or double 10 (10.0) then it will normally divide
        System.out.println(var/10.0); // 123.4

    }
}
