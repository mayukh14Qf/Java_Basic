package Day3;

public class Arithmetic {
    public static void main(String[] args) {
        //  It will perform the arithmetic operations
        int varOne=78;
        int varTwo=89;
        int answer=varTwo+varOne; // operation performing
        System.out.println(answer); // printing

        System.out.println(10+89);
        System.out.println(8-7);
        System.out.println(78*2);
        System.out.println(88/4);

        // Modulo ------ > 87/2 -----> q : 43  , r : 1
        //                 87/2 -----> 43.5 (float)
        System.out.println(87/2); // 43 (Q)
        //Modulo --- > It will print Reminder
        System.out.println(3/2); // 1 (Q)
        System.out.println(3%2); // 1(R)

        System.out.println(10/6); // 1 (Q)
        System.out.println(10%6); // 4 (R)

        System.out.println(87%2); // 1 (R)
        System.out.println(82%4); // 2 (R)


        // N: B:
        System.out.println(10/6); // 1(Q)
        // Question
        double a=10;
        double b=6;
        System.out.println(a/b);
        // 10.0 ==== 10
        // decimal/decimal -----> Total Answer (float/Double)
        System.out.println(10.0/6.0);

    }
}
